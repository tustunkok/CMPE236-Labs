; GLOBAL 7SEG CONSTANTS
N0		EQU	11000000B		; #DEFINE N0 11000000B
N1		EQU	11111001B
N2		EQU	10100100B
N3		EQU	10110000B
N4		EQU	10011001B
N5		EQU	10010010B
N6		EQU	10000010B
N7		EQU	11111000B
N8		EQU	10000000B
N9		EQU	10010000B
NA		EQU	10001000B
NB		EQU	10000011B
NC		EQU	11000110B
ND		EQU	10100001B
NE_		EQU	10000110B
NF		EQU	10001110B

		ORG	00H
		SJMP	MAIN

		ORG	30H
MAIN:		MOV	P1, #0F0H

		ACALL	WAIT

		ACALL	FIND_COL

HALT:		SJMP	HALT

; ===================================================
; WAIT FOR AN INPUT
; PARAMS: NONE
; RETURN: NONE
WAIT:		MOV	A, P1
		ORL	A, #0FH
		CPL	A
		JZ	WAIT
		RET

; ===================================================
; GETS THE COLUMN VALUE AND CALCULATES THE EXACT BUTTON
; PARAMS: COLUMN INDEX
; RETURN: ASCII/VALUE OF THE CLICKED NUMBER

; ===================================================
; ACCORDING TO GIVEN PARAMETER FIND WHICH COL/ROW IS ACTIVATED
; PARAMS: STATE (0/1)
; RETURN: THE COLUMN/ROW NUMBER
FIND_COL_ROW:	CJNE	R2, #00H, PASS_ROW_DATA
		MOV	DPTR, #COL_DATA
		SJMP	CNT1
PASS_ROW_DATA:	MOV	DPTR, #ROW_DATA
CNT1:		MOV	R2, P1

		MOV	R3, #04D

LP2:		MOV	A, R3
		DEC	A
		MOVC	A, @A+DPTR
		CLR	C
		SUBB	A, R2
		JZ	FOUND
		DJNZ	R3, LP2

FOUND:		MOV	A, #04D
		CLR	C
		SUBB	A, R3
		RET

; ==================================================
WRT2LED:	MOV	DPTR, #LED_DATA
		MOV	A, #0FH
		CLR	C
		SUBB	A, R2
		JC	EXT1
		MOV	A, R2
		MOVC	A, @A+DPTR
		MOV	P2, A
EXT1:		RET

; ==================================================
		ORG	0200H
COL_DATA:	DB	70H, 0B0H, 0D0H, 0E0H
ROW_DATA:	DB	00H
LED_DATA:	DB	N0, N1, N2, N3, N4, N5, N6, N7, N8, N9, NA, NB, NC, ND, NE_, NF
		END