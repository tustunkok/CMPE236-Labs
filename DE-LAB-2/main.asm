; GLOBAL 7SEG CONSTANTS
N0		EQU	11000000B		; #DEFINE N0 11000000B
N1		EQU	11111001B
N2		EQU	10100100B
N3		EQU	10110000B
N4		EQU	10011001B
N5		EQU	10010010B
N6		EQU	10000010B
N7		EQU	11111000B
N8		EQU	10000000B
N9		EQU	10010000B
NA		EQU	10001000B
NB		EQU	10000011B
NC		EQU	11000110B
ND		EQU	10100001B
NE_		EQU	10000110B
NF		EQU	10001110B

; CONTSTANTS
ATTEMPT_LOC	EQU	20H
PASSWD_LOC	EQU	21H			; 21 22 23 24 -> PASSWORD
EN		EQU	P3.0

		ORG	00H
		SJMP	MAIN

		ORG	30H
MAIN:		MOV	ATTEMPT_LOC, #03D	; INT ATTEMPT = 3;

		ACALL	GET_PASSWD
		
HALT:		SJMP	HALT

; ===================================================
; CHECK THE ENTERED DIGITS BY THE USER
; PARAMS: NONE
; RETURN: NUMBER OF CORRECTLY ENTERED DIGITS


; ===================================================
; GET 4 DIGIT PASSWORD FROM USER
; PARAMS: NONE
; RETURN: NONE
GET_PASSWD:	MOV	R2, #04D		; I = 4;
		MOV	R0, #PASSWD_LOC
LP1:		JB	EN, LP1
		MOV	@R0, P2
		INC	R0
		DJNZ	R2, LP1
		RET

; ===================================================
; WRITE THE GIVEN NUMBER TO THE 7SEG DISPLAY
; PARAMS: NUMBER
; RETURN: NONE
WRT2LED:	MOV	DPTR, #LED_DATA
		MOV	A, #0FH
		CLR	C
		SUBB	A, R2
		JC	EXT1
		MOV	A, R2
		MOVC	A, @A+DPTR
		MOV	P1, A
EXT1:		RET

		ORG	0200H
LED_DATA:	DB	N0, N1, N2, N3, N4, N5, N6, N7, N8, N9, NA, NB, NC, ND, NE_, NF
		END